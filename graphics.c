uint8_t send_byte_spi(uint8_t byte)
{
	uint8_t byteReturn;
	
	while ((SPI2STAT & PIC32_SPISTAT_SPITBE) == 0);

	
	SPI2BUF = byte;
	

	while ((SPI2STAT & PIC32_SPISTAT_SPIRBF) == 0);	
	
	byteReturn = SPI2BUF;
	return byteReturn;
}



void init_oled()
{
	SPI2CON = 0;
	SPI2BRG = 15; 
	SPI2STATCLR = PIC32_SPISTAT_SPIROV;
	SPI2CONSET = PIC32_SPICON_CKP;
	SPI2CONSET = PIC32_SPICON_MSTEN;
	SPI2CONSET = PIC32_SPICON_ON;
	
	
	PORTFSET = 0x40|0x20|0x10;

	PORTF = 0xFFFF;
	PORTG = (1 << 9);
	ODCF = 0x0;
	ODCG = 0x0;
	TRISFCLR = 0x70;
	TRISGCLR = 0x200;


	PORTGSET = 0x200;
}


